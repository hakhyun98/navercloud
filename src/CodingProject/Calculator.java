package CodingProject;

public class Calculator {

	public double add(double num, double num1) {
		double result = num + num1;
		return result;
	}

	public double subtract(double num, double num1) {
		double result = num - num1;
		return result;
	}

	public double multiply(double num, double num1) {
		double result = num * num1;
		return result;
	}

	public double division(double num, double num1) {
		double result = num / num1;
		return result;
	}
}
